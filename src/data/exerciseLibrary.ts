import { exerciseGroups } from "./exercises";

export type Difficulty = "iniciante" | "intermediario" | "avancado";

export interface ExerciseInfo {
  /** Grupo muscular principal (mesmo nome usado em exerciseGroups). */
  group: string;
  /** Músculos primários trabalhados, em PT-BR. */
  primaryMuscles: string[];
  /** Músculos auxiliares/estabilizadores. */
  secondaryMuscles?: string[];
  /** Equipamento necessário. */
  equipment: string;
  difficulty: Difficulty;
  /** Passo a passo de execução. */
  instructions: string[];
  /** Dicas de técnica e erros comuns. */
  tips?: string[];
  /** [posição inicial, posição final] — alternadas pela UI para simular o movimento. */
  images?: [string, string];
}

const CDN = "https://cdn.jsdelivr.net/gh/yuhonas/free-exercise-db@main/exercises";

function img(id: string): [string, string] {
  return [`${CDN}/${id}/0.jpg`, `${CDN}/${id}/1.jpg`];
}

