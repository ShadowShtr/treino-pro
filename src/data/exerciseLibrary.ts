/** Retorna o grupo muscular de um exercício do catálogo (ou "" se for custom). */
export function getExerciseGroup(name: string): string {
  for (const { group, exercises } of exerciseGroups) {
    if (exercises.includes(name)) return group;
  }
  return exerciseLibrary[name]?.group ?? "";
}
